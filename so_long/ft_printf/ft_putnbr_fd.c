#include "ft_printf.h"

void	putnbr_fd(int n, int fd)
{
	if (n == -2147483648)
		putstr_fd("-2147483648", fd);
	else
	{
		if (n < 0)
		{
			putchar_fd('-', fd);
			n = n * -1;
		}
		if (n >= 9)
		{
			putnbr_fd(n / 10, fd);
		}
		putchar_fd((n % 10) + '0', fd);
	}
}
