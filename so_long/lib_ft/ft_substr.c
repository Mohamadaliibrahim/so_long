#include "libft.h"

char	*ft_substr(const char *s, unsigned int start, size_t len)
{
	char	*ch;
	size_t	s_len;

	if (!s)
		return (NULL);
	s_len = ft_strlen(s);
	if (start >= s_len)
		return (ft_strdup(""));
	if (s_len - start < len)
		len = s_len - start;
	ch = (char *)malloc(sizeof(char) * (len + 1));
	if (!ch)
		return (NULL);
	ft_strlcpy(ch, s + start, len + 1);
	return (ch);
}
